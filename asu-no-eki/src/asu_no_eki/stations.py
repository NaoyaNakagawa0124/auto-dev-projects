"""Yamanote line — 30 stations × 3-4 urban-planning events each.

Events are gentle one-liners about ongoing construction, redevelopments, openings,
and transit changes. They lean on publicly-known plans circa mid-2026 with
some plausible filler — the goal is to make the commute feel less static, not
to be a news service.
"""

from __future__ import annotations

from dataclasses import dataclass


EVENT_TYPES = ("redev", "open", "close", "transit", "construction")


@dataclass(frozen=True)
class Event:
    year: int
    month: int
    type: str       # one of EVENT_TYPES
    blurb: str      # <= 60 jp chars


@dataclass(frozen=True)
class Station:
    id: str
    jp: str
    kana: str
    ring_index: int   # 0..29, clockwise from Tokyo
    events: tuple[Event, ...]


def _ev(year: int, month: int, type: str, blurb: str) -> Event:
    return Event(year=year, month=month, type=type, blurb=blurb)


