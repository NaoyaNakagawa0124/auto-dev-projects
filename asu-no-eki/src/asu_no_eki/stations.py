def by_id(station_id: str) -> Station | None:
    for s in STATIONS:
        if s.id == station_id:
            return s
    return None


def in_ring_order() -> tuple[Station, ...]:
    return tuple(sorted(STATIONS, key=lambda s: s.ring_index))
