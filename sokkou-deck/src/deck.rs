use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Card {
    pub id: String,
    pub prompt: String,
    pub answers: Vec<String>,
    pub note: Option<String>,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Deck {
    pub id: String,
    pub name: String,
    pub description: String,
    pub cards: Vec<Card>,
}

impl Deck {
    pub fn card_count(&self) -> usize {
        self.cards.len()
    }
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct DeckSummary {
    pub id: String,
    pub name: String,
    pub description: String,
    pub card_count: usize,
}

impl From<&Deck> for DeckSummary {
    fn from(d: &Deck) -> Self {
        Self {
            id: d.id.clone(),
            name: d.name.clone(),
            description: d.description.clone(),
            card_count: d.card_count(),
        }
    }
}

fn c(id: &str, prompt: &str, answers: &[&str]) -> Card {
    Card {
        id: id.to_string(),
        prompt: prompt.to_string(),
        answers: answers.iter().map(|s| s.to_string()).collect(),
        note: None,
    }
}

pub fn all_decks() -> Vec<Deck> {
    vec![toeic_600(), it_pass(), elements(), jlpt_n1_idioms(), prime_ministers()]
}

pub fn find_deck(id: &str) -> Option<Deck> {
    all_decks().into_iter().find(|d| d.id == id)
}

