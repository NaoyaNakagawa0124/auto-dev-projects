def _interleave_by_category(raw: list[tuple[str, str]]) -> list[tuple[str, str]]:
    """Reorder so categories rotate day-by-day rather than blocking together.

    7-day cycle: philosophy → people → money → failure → tools → era → legacy
    → philosophy → ... so a one-week demo sees all 7 categories naturally.
    """
    cat_order = ["philosophy", "people", "money", "failure", "tools", "era", "legacy"]
    buckets: dict[str, list[tuple[str, str]]] = {c: [] for c in cat_order}
    for cat, text in raw:
        if cat in buckets:
            buckets[cat].append((cat, text))
    out: list[tuple[str, str]] = []
    i = 0
    while any(buckets[c] for c in cat_order):
        cat = cat_order[i % len(cat_order)]
        if buckets[cat]:
            out.append(buckets[cat].pop(0))
        i += 1
    return out


_INTERLEAVED = _interleave_by_category(_RAW)


QUESTIONS: tuple[Question, ...] = tuple(
    Question(id=i, category=cat, text=text)
    for i, (cat, text) in enumerate(_INTERLEAVED)
)


def total() -> int:
    return len(QUESTIONS)


def question_for(d: date) -> Question:
    """Map a date deterministically to a question.

    Uses day-of-year minus 1 mod total. Leap day cycles back to question 0
    (acceptable — it's rare and a Q can repeat once every ~4 years).
    """
    doy = d.timetuple().tm_yday
    idx = (doy - 1) % len(QUESTIONS)
    return QUESTIONS[idx]


def by_category(cat: str) -> list[Question]:
    return [q for q in QUESTIONS if q.category == cat]
