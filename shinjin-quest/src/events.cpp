Event getSeasonalEvent(const DayInfo& day) {
    // GW
    if (day.month == 5 && day.dayOfMonth == 3) {
        return {
            "🎌 ゴールデンウィーク！",
            "待ちに待ったGW！新人にとって初めての長期休暇。",
            {
                {"旅行に行く", 10, 25, -30000, 0, 0, "最高のリフレッシュ！メンタル大回復。"},
                {"実家に帰る", 15, 20, -10000, 0, 0, "久しぶりの故郷。家族の温かさを実感した。"},
                {"家でゴロゴロ", 20, 15, -3000, 0, 0, "何もしない贅沢。体力もメンタルも回復。"},
            }
        };
    }
    // Summer bonus
    if (day.month == 6 && day.dayOfMonth == 25) {
        return {
            "💰 夏のボーナス！",
            "初めてのボーナスが振り込まれた！",
            {
                {"貯金する", 0, 5, 150000, 0, 0, "堅実な選択。将来の自分に感謝される。"},
                {"パーッと使う", 5, 20, 50000, 0, 0, "自分へのご褒美！幸福度UP！"},
            }
        };
    }
    // Obon
    if (day.month == 8 && day.dayOfMonth == 13) {
        return {
            "🏮 お盆休み",
            "お盆の連休開始。地元に帰ろうか、それとも...？",
            {
                {"帰省する", 10, 20, -15000, 0, 0, "故郷の空気は最高だ。"},
                {"一人で満喫", 15, 10, -5000, 0, 1, "空いた東京を堪能した。"},
            }
        };
    }
    // Year end
    if (day.month == 12 && day.dayOfMonth == 25) {
        return {
            "💰 冬のボーナス！",
            "冬のボーナス支給日！",
            {
                {"貯金する", 0, 5, 200000, 0, 0, "1年目にしてはかなり貯まった。"},
                {"ちょっと贅沢", 5, 15, 100000, 0, 0, "自分を労おう。"},
            }
        };
    }
    // New Year
    if (day.month == 1 && day.dayOfMonth == 1) {
        return {
            "🎍 あけましておめでとう！",
            "新しい年が始まった。もうすぐ社会人2年目だ。",
            {
                {"今年の目標を立てる", 5, 10, 0, 3, 3, "新年の抱負。やる気がみなぎる！"},
            }
        };
    }
    return {"", "", {}};
}
