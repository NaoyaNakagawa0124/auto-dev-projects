std::vector<Choice> getWorkChoices(const Player& p, const DayInfo& day) {
    std::vector<Choice> choices;

    choices.push_back({
        "普通に定時で帰る",
        -5, -3, 0, 1, 1,
        "今日も一日お疲れさまでした。"
    });

    choices.push_back({
        "残業して頑張る",
        -15, -10, 3000, 5, 3,
        "遅くまでお疲れさま。上司に「頑張ってるね」と言われた。"
    });

    choices.push_back({
        "有給を使って休む",
        20, 15, 0, -2, 0,
        "ゆっくり休めた。心も体もリフレッシュ。"
    });

    if (p.mp < 40) {
        choices.push_back({
            "早退する（体調不良）",
            10, 10, 0, -3, 0,
            "無理は禁物。早めに切り上げた。"
        });
    }

    // Season-specific
    if (day.season == Season::SUMMER && p.hp < 50) {
        choices[0].hpDelta -= 3; // Summer heat penalty
        choices[0].result = "暑さでバテ気味...。定時で帰れてよかった。";
    }

    return choices;
}

